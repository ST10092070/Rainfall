package com.corne.rainfall.data.model

/**
 * This enum class represents the type of a notification.
 * It has four possible values: WARNING, INFORMATION, DANGER, GENERAL.
 */
enum class NotificationType {
    /**
     * This value represents a warning notification type.
     */
    WARNING,

    /**
     * This value represents an information notification type.
     */
    INFORMATION,

    /**
     * This value represents a danger notification type.
     */
    DANGER,

    /**
     * This value represents a general notification type.
     */
    GENERAL
}